        ;lda #$0a : sta $211c
        ;rep #$20
        ;lda $2134
        ;lda.l OWEdgeTable,a
        ;nop

    plx : dex : pla : inc : pha : txa : bra .nextTransition


    ;Infinite loop at Link's
    ;lda #$2c
    .noTransition
    pla
    sep #$30

    jsl OWVanilla
    bra .return

    .return
    rtl
}

