OWShuffle:
{
    ;Assume you're at links house = $2c
    ;transitioning right will result in X = $2d
    ;transitioning left will result in X = $2b
    ;up X = $24
    ;down X = $34

    ;compares X to determine direction of edge transition
    lda $700 : lsr : sta $700
    cpx $700 : bcc .upOrLeft
    dex : cpx $700 : inx : bne .downEdge
    bra .rightEdge
    .upOrLeft
    inx : cpx $700 : dex : bne .upEdge
    bra .leftEdge

    ;sets new OWID and coords
    .upEdge
    lda #$0 : sta $418
    ;inc $21 : inc $21
    ;inc $e7 : inc $e7
    ;inc $e9 : inc $e9
