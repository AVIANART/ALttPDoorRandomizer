    ;inc $613 : inc $613
    ;lda $700 : clc : adc #$10 : sta $700
    bra .setOWID

    .downEdge
    lda #$1 : sta $418
    ;dec $21 : dec $21
    ;dec $e7 : dec $e7
    ;dec $e9 : dec $e9
    ;dec $611 : dec $611
    ;dec $613 : dec $613
    ;lda $700 : sec : sbc #$10 : sta $700
    bra .setOWID

    .leftEdge
    lda #$2 : sta $418
    ;inc $23 : inc $23
    ;inc $e1 : inc $e1
    ;inc $e3 : inc $e3
    ;inc $615 : inc $615
    ;inc $617 : inc $617
    ;inc $700 : inc $700
    bra .setOWID

    .rightEdge
    lda #$3 : sta $418
    ;dec $23 : dec $23
    ;dec $e1 : dec $e1
    ;dec $e3 : dec $e3
    ;dec $615 : dec $615
    ;dec $617 : dec $617
    ;dec $700 : dec $700
    bra .setOWID

    .setOWID
    ;look up transitions in current area in table OWEdgeOffsets
    ;offset is (8bytes * OW Slot ID() + (2byte * direction slot)
    rep #$30
    asl : pha ;2 bytes per direction
    lda $8a : and #$0040 : adc $700 : asl #3
    adc 1,S : tax
    pla
    
    lda #0
    pha
    tsx
    lda.l OWEdgeOffsets,x ;load value from offsets table
    and #$000f
    
    .nextTransition
    beq .noTransition ;number of transitions
    pha

        ;lda.l OWEdgeOffsets,x : ror #4 : and #$0fff : tax ;record id of first transition in table
